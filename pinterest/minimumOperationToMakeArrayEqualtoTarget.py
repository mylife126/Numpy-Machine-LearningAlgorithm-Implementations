'''
这一题目的是说每次一操作对于nums的时候 可以全部数组操作，或者区间内操作，或者一个一个index操作， 这个叫做subarray。

那么问你最少操作几次，可以见的我们肯定希望每次操作都是区间越长越好。

那我们可以把问题抽象成 算出diff 后 如何通过+1 -1 得到【0，0，0，0， 0.。。。】
也就是砍高度或增高度。

假设
Diff = [3,1,1,1]
可见 diff[0] > diff[1] 2 3
所以第一次operation可以直接全部 -1 得到 【2，0，0，0】
那么接下来只能选取 index 0 做操作， 连续减少两次1， 所以operation =3
这里面可以看到一个规律，diff【0】决定了minimal的操作次数。


现在抓到的方向已经很接近了，但你那句“我们肯定希望每次操作都是区间越长越好”只对了一半。更准确地说，不是“盲目选最长区间”，而是“只要一段区间里所有位置还需要往同一个方向调整，就尽量把它们绑在一起做”。这两句话很像，但差别很关键。因为有时候区间虽然长，但中间有人该加、有人该减，或者有人已经到位了，那就不能一起做。 也就是说还有一个case是方向改变了，例如 [3, 1, -2, 1], 那么到了-2这个地方他的操作是完全独立的，因为每一次operation只能+1 或者 -1 而他需要+1. 所以这个case下， 只有【3 1 】一起操作了

我们从头来。题目说一次操作可以选任何 subarray，也就是任意连续区间。这个区间可以是整个数组，也可以是一小段，也可以只包含一个元素。然后这一次操作对这段区间里所有元素统一做同样的事：要么全部加 1，要么全部减 1。题目问最少多少次才能把 nums 变成 target。

所以真正要想的不是“怎么改 nums”，而是“每个位置到底还差多少”。这就是为什么第一步一定要做 diff：

diff[i] = target[i] - nums[i]

如果 diff[i] 是正数，表示这个位置还需要补这么多次 +1。如果 diff[i] 是负数，表示这个位置还需要做这么多次 -1。如果是 0，就已经好了。

于是原题就变成了：我有一个 diff 数组，我每次可以选一个连续区间，让这一段统一朝着同一个方向少 1 个需求。最后目标是把整个 diff 变成全 0。

你说把它看成“砍高度或增高度”，这个直觉是完全对的。只是要再补一句：正数部分和负数部分其实是两种相反方向的“高度”，它们不能混着一起做。因为一次操作只能选整段统一加，或者整段统一减，不可能一边加一边减。

现在看你举的例子：

diff = [3,1,1,1]

你的想法是：第一次可以整个数组一起减 1，得到 [2,0,0,0]，然后再对 index 0 单独减两次，所以总共 3 次。这个结论是对的。这里你已经观察到了一个很重要的现象：第一个位置的 3，的确决定了至少要有 3 次操作，因为第 0 位无论如何都得被改 3 次。更妙的是，后面的 1、1、1 可以“搭便车”，也就是说，在你为了处理第 0 位而做的那 3 次操作里，其中有 1 次可以顺手把后面这三个位置也一起处理掉。剩下那 2 次因为后面已经归零了，就只能留给第 0 位自己。

但这里真正的规律不是“因为 diff[0] 最大，所以能整段做”。更准确的规律是：只要后面的值和前面是同方向，而且高度不超过前面，那么前面为了处理自己而做的那些操作，就可以顺手覆盖后面。你的这个例子里，3,1,1,1 全是正的，而且后面都不高于前面，所以第一个位置的操作自然可以带着后面一起做一次。

我们再看一个你刚刚也问过的关键例子：

diff = [3,2,1]

你刚刚卡住的点是：为什么 2 和 1 可以被前面的 3 “带着削”。现在我们直接模拟。你要把 [3,2,1] 变成 [0,0,0]。第一次你当然会选整个区间 [0..2] 减 1，变成 [2,1,0]。第二次你还是可以选 [0..1] 或者 [0..2] 都行，最自然的是对还没变成 0 的那一段继续减，得到 [1,0,0]。第三次只剩第 0 位，再减一次，变成 [0,0,0]。总共 3 次。

这里“带着削”的意思就是：你本来是为了处理最左边那个 3，不得不做 3 次减法，而在这 3 次减法里的前 2 次，刚好也覆盖到了第二个位置；前 1 次，刚好也覆盖到了第三个位置。因为它们的需求都没有超过左边，所以左边在“开工”的过程中，就已经顺手帮它们干完了。它们不需要自己额外再开一份工。

所以你可以把“同方向且不更高”理解成：“后面的这个位置，不需要一个新的施工队，它可以蹭前面施工队的工作”。

然后我们再看一个反例，这个反例最能帮助你理解为什么不能只盯着 diff[0]。比如：

diff = [1,3,2]

这时候第一个位置只需要 1 次，但答案显然不是 1。为什么？因为第 1 位比前面高。你第一次可以对 [0..2] 整段减 1，得到 [0,2,1]。到这里，第 0 位已经处理完了，但第 1 位和第 2 位还没完。接下来你还得继续对后面那段开工。所以这里的真实逻辑是：第一个位置给了你“至少要开工 1 次”，然后当你走到第二个位置，发现它从 1 涨到了 3，多出来的 2 是前面的那 1 次操作覆盖不到的，所以这 2 必须额外新增操作。再走到第三个位置，2 没有比前面的 3 更高，所以它不需要再新增；因为处理第二个位置的那几次操作，已经顺手把第三个位置也覆盖到了。

所以对于一整段同号的情况，你真正关心的是“向上长出来的台阶”。第一根柱子有多高，你至少要做那么多次；后面如果没比前面高，就不需要新增；只有当后面比前面更高时，多出来的那一截，才要补操作。

现在再看一个更重要的 case，就是方向变化。比如：

diff = [3,0,0,-2]

这个例子能说明为什么“最长区间优先”不是绝对规则。左边的 3 表示第 0 位需要做 3 次 +1，右边的 -2 表示第 3 位需要做 2 次 -1。这两件事方向完全相反。你不可能说“我选一个长区间把它们一起做了”，因为一次操作只能统一加，或者统一减，不可能这边补、那边削。所以这里就必须分成两段独立工作：左边那段自己做 3 次，右边那段自己做 2 次，总共 5 次。你不能让一个方向的操作去帮助另一个方向的需求。

所以从这个例子你应该建立一个非常重要的认知：一旦 diff 的符号变了，前面的操作就彻底帮不上后面了。因为前面那段是在做“加法工程”，后面那段是在做“减法工程”，它们是两个不同施工队，不能共享工作量。

到这里，其实整个算法逻辑已经出来了。我们从左往右看 diff。第一个位置的绝对值，肯定是必须付出的初始工作量，因为它没有左边可以借力。然后继续往右看每一个位置。如果它和前一个位置是同方向的，也就是同号，那它有机会借前一个位置的操作。如果它的绝对值没有超过前一个位置，说明前面的施工已经足够带着它一起做完，它不用新增操作。如果它的绝对值比前一个位置大，说明它比前面“长出来一截”，这一截必须单独新增操作。如果它和前一个位置方向不同，那前面的施工完全帮不上它，它就得从 0 开始重新算自己的那部分工作量。

这就是为什么代码会写成“同号就看绝对值是否上涨，异号就重新加当前绝对值”。


我们最后再用几个不同 case 快速串一下，这样你会更稳。

如果 diff = [3,1,1,1]，第一个位置先给 3 次。后面都是同号，而且都没有超过前一个，所以不新增。答案就是 3。意思是：后面的这些小需求都可以蹭第一个位置的施工。

如果 diff = [1,3,2]，第一个位置给 1 次。第二个位置同号，但从 1 长到 3，多出 2，所以补 2。第三个位置同号，但从 3 降到 2，不补。答案 3。意思是：只有“长出来的台阶”要额外开工。

如果 diff = [3,0,0,-2]，第一个位置给 3 次。后面两个 0 不需要新增。到了 -2，方向变了，所以重新加 2。答案 5。意思是：正负两边是独立工作。

如果 diff = [-2,-4,-1]，第一个位置先给 2 次。第二个位置和前一个同号，且从 2 增到 4，多出 2，所以补 2。第三个位置同号，但从 4 降到 1，不补。答案 4。虽然是负数，但逻辑和正数完全一样，只是方向变成了“往下压”。

所以你最后可以把这题记成一句非常准确的话：这题不是在搜路径，也不是在暴力选区间，而是在看 diff 这排需求里，哪些位置可以共享前面的区间操作，哪些位置必须单独新增操作。同方向的时候，后面的矮柱子可以蹭前面的操作；不同方向的时候，必须另起炉灶。
'''

"""
nums = [3, 5, 1, 2] target = [4, 6, 2, 4]
diff = [1, 1, 1, 2]

rule 1: min op = diff[0] = 1, 因为我们的目标是能尽量多的subarry长度下做砍头，那么最左边的高度限制了下限。 因为你不管怎样头上的高度或者低度你都得添加

rule2: 再看diff[i] and diff[i-1] where i>=1 ，可以这样理解只要diff【i】 小于前一个，那么他们就是一体的，可以根据其左边的那个一起被砍掉或者增高，例如diff = [3,2,1] 那我在看3的时候2也会被砍1也是，然后变成 2 1 0， 我继续砍2的时候 1 也可以被砍 所以只用看什么时候diff[i] > diff[i-1]了 就是一个新的开端而规则是:
 count += abs(diff[i]) - abs(diff[i-1]) if diff[i]>diff[i-1]
 else: does not change
例如[3,5], 那么3砍完后，5变成了2， 不能连带着砍下去了，剩余 diff[1] - diff[0] = 2则 total += 2

rule 3: 其次是方向改变了，也是一个新的开端他也不能随着左边一起操作例如
[3,1,-2,-1] [3,1] 可以看成为一个整体一起砍3次 但是到了-2 是不可以的，-2成为了一个新的开端 :
count += abs(diff[where_change_start])

再看【-2, -1】 这个是同一个情况，我们都是看绝对高度 所以此刻diff【i】< diff[i-1] apply rule2


"""


class Solution(object):
    def minimumOperations(self, nums, target):
        """
        先把问题转换成 diff：
        diff[i] = target[i] - nums[i]

        diff[i] > 0 说明这个位置还需要做这么多次 +1
        diff[i] < 0 说明这个位置还需要做这么多次 -1

        一次操作可以选择一个连续区间，让这一整段统一 +1 或 -1。
        所以从左到右看：
        - 第一个位置必须自己开工 abs(diff[0]) 次
        - 如果当前和前一个同号，那么当前可以复用前一个的操作，
          只有“比前一个多出来的那部分”才需要额外新增操作
        - 如果当前和前一个异号，说明方向变了，前面的操作帮不上当前，
          当前这段必须重新开工 abs(diff[i]) 次
        """
        diff = [target[i] - nums[i] for i in range(len(target))]
        operations = 0

        # we use abs for standardize the -/ +
        min_operation = abs(diff[0])
        operations = min_operation
        # now start the rest
        for i in range(1, len(diff)):
            prev = diff[i - 1]
            current = diff[i]

            # judge if the sign changes
            if prev * current > 0:
                # if still the same sign and the diff[i] > diff[i -1]
                # we need to compensate the height difference
                if abs(current) > abs(prev):
                    operations += abs(current) - abs(prev)

            # sign changes, apply rule 3
            else:
                operations += abs(current)

        return operations